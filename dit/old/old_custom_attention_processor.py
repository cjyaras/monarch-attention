class CustomAttention(Attention):
    """
    Custom attention class to include efficient attention types
    """

    def __init__(
        self,
        query_dim: int,
        cross_attention_dim: Optional[int] = None,
        heads: int = 8,
        kv_heads: Optional[int] = None,
        dim_head: int = 64,
        dropout: float = 0.0,
        bias: bool = False,
        upcast_attention: bool = False,
        upcast_softmax: bool = False,
        cross_attention_norm: Optional[str] = None,
        cross_attention_norm_num_groups: int = 32,
        qk_norm: Optional[str] = None,
        added_kv_proj_dim: Optional[int] = None,
        added_proj_bias: Optional[bool] = True,
        norm_num_groups: Optional[int] = None,
        spatial_norm_dim: Optional[int] = None,
        out_bias: bool = True,
        scale_qk: bool = True,
        only_cross_attention: bool = False,
        eps: float = 1e-5,
        rescale_output_factor: float = 1.0,
        residual_connection: bool = False,
        _from_deprecated_attn_block: bool = False,
        processor: Optional["AttnProcessor"] = None,
        out_dim: int = None,
        out_context_dim: int = None,
        context_pre_only=None,
        pre_only=False,
        elementwise_affine: bool = True,
        is_causal: bool = False,
        efficient_attention_type: str = "softmax"
    ):
        super(CustomAttention, self).__init__(
            query_dim,
            cross_attention_dim,
            heads,
            kv_heads,
            dim_head,
            dropout,
            bias,
            upcast_attention,
            upcast_softmax,
            cross_attention_norm,
            cross_attention_norm_num_groups,
            qk_norm,
            added_kv_proj_dim,
            added_proj_bias,
            norm_num_groups,
            spatial_norm_dim,
            out_bias,
            scale_qk,
            only_cross_attention,
            eps,
            rescale_output_factor,
            residual_connection,
            _from_deprecated_attn_block,
            processor,
            out_dim,
            out_context_dim,
            context_pre_only,
            pre_only,
            elementwise_affine,
            is_causal
        )

        ATTENTION_TYPE_TO_MODULE = {
            "soba": SobaMonarchAttnProcessor,
            "linformer": LinformerAttnProcessor,
            "cosformer": CosformerAttnProcessor,
            "performer": PerformerAttnProcessor,
            "nystromformer": NystromformerAttnProcessor
        }

        self.efficient_attention_type = efficient_attention_type
        self.efficient_attention_module = ATTENTION_TYPE_TO_MODULE[self.efficient_attention_type]
        self.set_processor(self.efficient_attention_module)

        # Has same foward() method as parent class 


    def set_processor(self, processor: torch.nn.module) -> None:
        r"""
        Set the attention processor to use.
        This differs from the parent class call since processor can be any torch.nn.Module rather than 
            an "AttnProcessor"

        Args:
            processor (`AttnProcessor`):
                The attention processor to use.
        """
        # if current processor is in `self._modules` and if passed `processor` is not, we need to
        # pop `processor` from `self._modules`

        if (
            hasattr(self, "processor")
            and isinstance(self.processor, torch.nn.Module)
            and not isinstance(processor, torch.nn.Module)
        ):
            logger.info(f"You are removing possibly trained weights of {self.processor} with {processor}")
            self._modules.pop("processor")

        self.processor = processor

